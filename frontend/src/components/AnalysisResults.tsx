// ============================================================
// Helpers
// ============================================================

function getConfidenceBadge(prob: number): { label: string; color: string; dot: string } {
  if (prob >= 70) return { label: "High Confidence", color: "bg-emerald-100 text-emerald-700", dot: "🟢" };
  if (prob >= 30) return { label: "Moderate", color: "bg-amber-100 text-amber-700", dot: "🟡" };
  return { label: "Low Probability", color: "bg-slate-100 text-slate-500", dot: "⚪" };
}

function getBarColor(prob: number): string {
  if (prob >= 70) return "bg-emerald-500";
  if (prob >= 30) return "bg-amber-500";
  return "bg-slate-400";
}

function generateClinicalSummary(result: AnalysisResult): string {
  const top = result.top_predictions[0];
  const second = result.top_predictions[1];
  const topConf = top?.probability?.toFixed(2) ?? "N/A";
  const topDisease = top?.disease ?? "Unknown";
  const secondDisease = second?.disease;

  let summary = `The multimodal attention model predicts "${topDisease}" as the primary finding with a confidence of ${topConf}%. `;

  if (secondDisease && secondDisease !== topDisease) {
    summary += `A secondary differential of "${secondDisease}" was also noted at ${second.probability.toFixed(2)}% confidence. `;
  }

  summary += `The Grad-CAM visualization highlights the spatial regions of the X-ray that contributed most strongly to this classification. `;
  summary += `SHAP analysis indicates that patient age exerted the highest influence among the provided clinical metadata features, followed by view position and gender. `;
  summary += `These findings are generated by an attention-enhanced multimodal CNN and should be interpreted as decision-support information. `;
  summary += `Review by a qualified radiologist or clinician is strongly recommended before any clinical action.`;

  return summary;
}

