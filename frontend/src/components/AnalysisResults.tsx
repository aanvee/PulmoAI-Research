// ============================================================
// Prediction Card
// ============================================================

function PredictionCard({ prediction, rank }: { key?: React.Key; prediction: TopPrediction; rank: number }) {
  const badge = getConfidenceBadge(prediction.probability);
  const barColor = getBarColor(prediction.probability);

  return (
    <div className="p-4 bg-surface-container rounded-2xl border border-outline-variant space-y-3">
      <div className="flex items-start justify-between gap-2">
        <div className="flex items-center gap-2">
          <span className="w-6 h-6 rounded-full bg-primary/10 text-primary text-xs font-bold flex items-center justify-center flex-shrink-0">
            {rank}
          </span>
          <span className="font-sans text-sm font-bold text-on-surface leading-tight">
            {prediction.disease}
          </span>
        </div>
        <span className="font-mono text-base font-bold text-primary flex-shrink-0">
          {prediction.probability.toFixed(2)}%
        </span>
      </div>

      <div className="w-full bg-surface-container-low rounded-full h-2.5 overflow-hidden">
        <div
          className={`h-full ${barColor} rounded-full transition-all duration-1000 ease-out`}
          style={{ width: `${Math.min(prediction.probability, 100)}%` }}
        />
      </div>

      <div className="flex items-center gap-1.5">
        <span className="text-xs">{badge.dot}</span>
        <span className={`text-xs font-semibold px-2 py-0.5 rounded-full ${badge.color}`}>
          {badge.label}
        </span>
      </div>
    </div>
  );
}

